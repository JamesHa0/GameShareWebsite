package crf.invest.entity;

public class NotSupportCard implements java.io.Serializable {

	private static final long serialVersionUID = -7804049064219167174L;
	private Long cardId;            //主键ID
	
	private String cardNumSix;        //卡号前六位
	private String bankName;        //发卡行
	private String cardName;		//卡名
	public Long getCardId() {
		return cardId;
	}
	public void setCardId(Long cardId) {
		this.cardId = cardId;
	}
	public String getCardNumSix() {
		return cardNumSix;
	}
	public void setCardNumSix(String cardNumSix) {
		this.cardNumSix = cardNumSix;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getCardName() {
		return cardName;
	}
	public void setCardName(String cardName) {
		this.cardName = cardName;
	}
	@Override
	public String toString() {
		return "NotSupportCard [cardId=" + cardId + ", cardNumSix="
				+ cardNumSix + ", bankName=" + bankName + ", cardName="
				+ cardName + "]";
	}

}