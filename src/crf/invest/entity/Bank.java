public class Bank implements java.io.Serializable {

	private static final long serialVersionUID = 259279723498009893L;
	
	private Long bankId;            //主键ID
	private String bankName;        //银行名称
	private String bankType;        //交易渠道  1 富友 2 银联 3 通联 4 财付通
	private String bankCode;        //银行代码
	private String bankEnabled;     //是否可用 1 是 0 否 默认1
	
	private String bankdanbi;       //单笔限额
	private String bankdanri;		//单日
	private String bankdanyue;		//单月限额
	
	public String getBankdanbi() {
		return bankdanbi;
	}
	public void setBankdanbi(String bankdanbi) {
		this.bankdanbi = bankdanbi;
	}
	public String getBankdanri() {
		return bankdanri;
	}
	public void setBankdanri(String bankdanri) {
		this.bankdanri = bankdanri;
	}
	public String getBankdanyue() {
		return bankdanyue;
	}
	public void setBankdanyue(String bankdanyue) {
		this.bankdanyue = bankdanyue;
	}
	public Long getBankId() {
		return bankId;
	}
	public void setBankId(Long bankId) {
		this.bankId = bankId;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBankType() {
		return bankType;
	}
	public void setBankType(String bankType) {
		this.bankType = bankType;
	}
	public String getBankCode() {
		return bankCode;
	}
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}
	public String getBankEnabled() {
		return bankEnabled;
	}
	public void setBankEnabled(String bankEnabled) {
		this.bankEnabled = bankEnabled;
	}

}