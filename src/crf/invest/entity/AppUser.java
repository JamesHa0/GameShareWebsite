	public String getUserRegisterDateStr(){
		if(this.userRegisterDate == null)
			return "";
		return DateUtil.dateToStr(this.userRegisterDate, "yyyy-MM-dd");
	}
	public String getUserCreateTimeStr(){
		if(this.userCreateTime == null)
			return "";
		return DateUtil.dateToStr(this.userCreateTime, "yyyy-MM-dd HH:mm:ss");
	}
	/**
	 * 	功能:是否绑定
	 *	王浩  @2015-9-7
	 */
	public String getIsBindDesc(){
		if(this.userWechatNo==null || "".equals(userWechatNo))
			return "未绑定";
		else
			return "已绑定";
	}
	/**
	 * 	功能:来源描述
	 *	王浩  @2015-9-7
	 */
	public String getUserRegSourceDesc(){
		if("1".equals(this.userRegSource)){
			return "微信";
		}else if("2".equals(this.userRegSource)){
			return "PC";
		}else {
			return "APP";
		}
	}
